const resetFavicon = () => {
  const icons = document.querySelectorAll('link[rel*=\'icon\']');
  icons.forEach(icon => {
    icon.href = icon.href.replace('dark', 'light');
  });
}

const init = () => {
  const params = new URL(window.location).searchParams;

  if (params.has('theme')) {
    document.body.classList.add(`theme_${params.get('theme')}`);

    if (params.get('theme') === 'light') {
      resetFavicon();
    }
  } else {
    document.body.classList.add(`theme_dark`);
  }

  const slide = Number.parseInt(params.get('slide')) || 0;
  document.title = data[slide].data.title;
  document.body.innerHTML += window.renderTemplate(data[slide].alias, data[slide].data);

  window.addEventListener('resize', () => {
    document.body.innerHTML = window.renderTemplate(data[slide].alias, data[slide].data);
  });
}

